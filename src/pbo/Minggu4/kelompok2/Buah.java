package pbo.Minggu4.kelompok2;

public class Buah {
    private String beratBuah;
    private String kondisi;

    // Setter
    public void setBeratBuah(String berat) {
        this.beratBuah = berat;
    }

    public void setKondisi(String kondisi) {
        this.kondisi = kondisi;
    }

    // Getter
    public String getBeratBuah() {
        return this.beratBuah;
    }

    public String getKondisi() {
        return this.kondisi;
    }
}
